package com.epam.rd.autocode.hashtableopen816;

public class MyTable {
	private int keys;
	private Object vals;
	
	public MyTable(int keys, Object vals) {
		super();
		this.keys = keys;
		this.vals = vals;
	}

	public int getKeys() {
		return keys;
	}

	public Object getVals() {
		return vals;
	}
	
	

}
